#include "../game_arkanoid.h"

int			*ft_get_lenght(char **map)
{
	int *t;

	t = (int*)malloc(sizeof(int) * 3);

	t[0] = 0;
	while (map[t[0]][0] != '\0')
	{
		t[1] = 0;
		while (map[t[0]] && map[t[0]][t[1]])
		{
			t[1]++;
		}
		t[0]++;
	}
	return (t);
}
